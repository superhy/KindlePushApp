class account():
    
    def setacc_id(self, acc_id):
        self.acc_id = acc_id
    def getaccount_id(self):
        return self.acc_id
    
    def setacc_name(self, acc_name):
        self.acc_name = acc_name
    def getacc_name(self):
        return self.acc_name
    
    def setacc_password(self, acc_password):
        self.acc_password = acc_password
    def getacc_password(self):
        return self.acc_password
    
    def setuser_id(self, user_id):
        self.user_id = user_id
    def getuser_id(self):
        return self.user_id
    
    def setweb_id(self, web_id):
        self.web_id = web_id
    def getweb_id(self):
        return self.web_id
    
